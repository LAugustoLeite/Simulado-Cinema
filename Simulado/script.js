var btn = document.getElementById('orfa-btn');
var container = document.querySelector('container-orfa');

btn.addEventListener("click", function(){

    var container = document.getElementById("container-orfa");

    if(container.style.display ==="flex"){
        container.style.display = "none";
    } else{
        container.style.display = "flex";
    }

});

